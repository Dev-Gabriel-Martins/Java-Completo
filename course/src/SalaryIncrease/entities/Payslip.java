
package SalaryIncrease.entities;


public class Payslip {
    
    private String name;
    private Double salary;
    
    public Payslip(){
    
    }

    public String getName() {
        return name;
    }

    public Double getSalary() {
        return salary;
    }
    
    public void setName(String name){
        this.name = name;
    }
    
}
